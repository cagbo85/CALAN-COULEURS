    public function build()
    {
        return $this->from(config('mail.from.address'), config('mail.from.name'))
            ->to($this->order->email)
            ->subject('🔔 DRING ! Nouvelle commande validée sur la boutique (#'.$this->order->id.')')
            ->view('emails.admin-order-notification');
    }

